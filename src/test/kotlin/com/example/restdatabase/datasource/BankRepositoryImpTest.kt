package com.example.restdatabase.datasource

import org.assertj.core.api.Assertions.assertThat
import org.junit.jupiter.api.Assertions.*
import org.junit.jupiter.api.Test


internal class BankRepositoryImpTest {

    val dataSource = BankRepositoryImp()

    @Test
    fun `should return a list of banks` () {
        //when
        val banks = dataSource.getBanks()
        
        //then
        assertThat(banks).isNotEmpty()
    }

    @Test
    fun `should provide some mock data` () {

        //when
        val banks = dataSource.getBanks()

        //then
        assertThat(banks).allMatch {it.accountNumber.isNotEmpty()}

    }
}